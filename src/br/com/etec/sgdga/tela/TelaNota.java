    private void listarCliente() {
        ControleCliente cli = new ControleCliente();
        clientes = cli.listarClientes("%%");
        cbCliente.removeAllItems();
        for (int i = 0; i < clientes.size(); i++) {
            cbCliente.addItem(clientes.get(i).getNome());
        }
    }

    private void listarProduto() {
        ControleProduto prod = new ControleProduto();
        produtos = prod.listarProdutos("%%");
        cbProduto.removeAllItems();
        for (int i = 0; i < produtos.size(); i++) {
            cbProduto.addItem(produtos.get(i).getNome());
        }
    }

    public void removerItem(int linha) {
        try {

            if (tbNota.getSelectedRow() != -1) {

                double valorRemovido;
                double valorTotal;
                valorRemovido = Double.valueOf(tmNotas.getValueAt(linha, 3).toString());
                valorTotal = Double.valueOf(txtValor.getValue().toString());

                txtValor.setValue(valorTotal - valorRemovido);
                txtFinal.setValue(valorTotal - valorRemovido - Double.parseDouble(txtDesconto.getText()));
                tmNotas.removeRow(linha);
                item.remove(linha);
                valor = valorTotal - valorRemovido;

            } else {
                JOptionPane.showMessageDialog(this, "Selecionar o item a  ser removido");
            }
        } catch (HeadlessException e) {
            JOptionPane.showMessageDialog(this, e);
        }
    }

    private void cancelarVenda() {
        while (tmNotas.getRowCount() > 0) {
            tmNotas.removeRow(0);
        }
        valor = 0;
        nota.clear();
        this.dispose();
        txtValor.setValue(0);
        txtDesconto.setValue(0);
        txtFinal.setValue(0);
    }

    private boolean verificarQuantidade() {

        try {
            if (Integer.parseInt(txtQuantidade.getText()) > 0) {
                return true;
            }
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, ex, "Erro", 0);

        }

        return false;
    }

    private void incluirProduto() {
        if (verificarQuantidade()) {

            BasicItemNota vb = new BasicItemNota();

            if (produtos.get(cbProduto.getSelectedIndex()).getEstoque() > Integer.parseInt(txtQuantidade.getText())) {
                vb.setNota(Integer.parseInt(txtID.getText()));
                vb.setProduto(produtos.get(cbProduto.getSelectedIndex()).getId());
                vb.setQuantidade(Integer.parseInt(txtQuantidade.getText()));
                vb.setValorUni(produtos.get(cbProduto.getSelectedIndex()).getValor_final());
                vb.setSubTotal(produtos.get(cbProduto.getSelectedIndex()).getValor_final() * Integer.valueOf(txtQuantidade.getText()));

                item.add(vb);
                n = n + 1;
                String id = String.valueOf(n);
                String produto = produtos.get(cbProduto.getSelectedIndex()).getNome();
                String quantidade = txtQuantidade.getText();
                String tot = String.valueOf(produtos.get(cbProduto.getSelectedIndex()).getValor_final());
                String sub = String.valueOf(produtos.get(cbProduto.getSelectedIndex()).getValor_final() * Integer.parseInt(txtQuantidade.getText()));

                // String total;
                //    total = String.valueOf(produtos.get(cbProduto.getSelectedIndex()).getValor_final() * Integer.parseInt(quantidade));
                String[] campos = new String[]{id, produto, quantidade, tot, sub};
                tmNotas.addRow(campos);
                valor += produtos.get(cbProduto.getSelectedIndex()).getValor_final() * Integer.parseInt(quantidade);
                txtValor.setValue(valor);

                if (txtDesconto.getText().trim().length() > 0) {
                    txtFinal.setValue(valor - Integer.parseInt(txtDesconto.getText()));
                } else {
                    txtFinal.setValue(valor);
                }

            } else {
                JOptionPane.showMessageDialog(this, "Desculpe, nÃ£o temos esta quantidade no estoque\n" + produtos.get(cbProduto.getSelectedIndex()).getNome() + "  " + produtos.get(cbProduto.getSelectedIndex()).getEstoque(), "Erro", 0);
                txtQuantidade.requestFocus();
            }
        } else {
            JOptionPane.showMessageDialog(this, "Quantidade Invalida\nInserir outro valor", "Erro", 0);
            txtQuantidade.requestFocus();
        }

    }

    private void cadastrarVenda() {
        if (!nota.isEmpty()) {/////////////////////////////////////////////
            JOptionPane.showMessageDialog(this, "Incluir pelo menos um produto!!", "Erro", 0);
        } else {
            try {

                BasicNota no = new BasicNota();
                java.util.Date d = new java.util.Date();

                SimpleDateFormat formatod = new SimpleDateFormat("yyyy-MM-dd");
                Date data = Date.valueOf(formatod.format(d));

                hora = Calendar.getInstance();
                hh = hora.get(Calendar.HOUR_OF_DAY);
                mm = hora.get(Calendar.MINUTE);
                ss = hora.get(Calendar.SECOND);

                hora = Calendar.getInstance();
                hh = hora.get(Calendar.HOUR_OF_DAY);
                mm = hora.get(Calendar.MINUTE);
                no.setCliente(clientes.get(cbCliente.getSelectedIndex()).getId());
                no.setData(data);
                no.setHora(Time.valueOf(formatar(hh) + ":" + formatar(mm) + ":" + formatar(ss)));
                no.setPagamento(1);
                try {
                    no.setValor(Double.valueOf(txtValor.getText().trim()));
                    no.setDesconto(Double.valueOf(txtDesconto.getText().trim()));
                    no.setValorFinal(Double.valueOf(txtFinal.getText().trim()));
                } catch (NumberFormatException e) {
                    no.setValor(Float.valueOf(txtValor.getText().trim().replaceAll(",", ".")));
                    no.setDesconto(Float.valueOf(txtDesconto.getText().trim().replaceAll(",", ".")));
                    no.setValorFinal(Float.valueOf(txtFinal.getText().trim().replaceAll(",", ".")));
                }
                ControleNota c = new ControleNota();
                c.cadastrarNotas(no);

                ControleItemNota ven = new ControleItemNota();
                for (int i = 0; i < item.size(); i++) {

                    ven.cadastrarItemNotas(item.get(i));

                }

                JOptionPane.showMessageDialog(this, "Venda Cadastrada com sucesso", "Certo", 1);
                int res = JOptionPane.showConfirmDialog(this, "Deseja Imprimir a nota", "ConfirmaÃ§Ã£o", JOptionPane.YES_NO_OPTION, 2);
                if (res == JOptionPane.YES_OPTION) {
                    if (vd == null) {
                        vd = new relVenda();
                        vd.setVisible(true);
                        vd.Iniciar(Integer.parseInt(txtID.getText()));
                    } else {
                        vd.setVisible(true);
                        vd.setState(JFrame.NORMAL);
                        vd.Iniciar(Integer.parseInt(txtID.getText()));
                    }
                }
                cancelarVenda();
            } catch (HeadlessException e) {
                JOptionPane.showMessageDialog(this, "Erro ao Cadastrar venda", "Erro", 0);

            }
        }
    }

    private String formatar(int num) {
        formato = new DecimalFormat("00");
        return formato.format(num);
    }

