    protected void tbChequeLinhaSelecionada(JTable tb) {
        if (tb.getSelectedRow() != -1) {
            txtBanco.setText(cheques.get(tb.getSelectedRow()).getBanco());
            txtAgencia.setText(cheques.get(tb.getSelectedRow()).getAgencia());
            txtNumero.setText(cheques.get(tb.getSelectedRow()).getNumeroCheque());
            txtPrazo.setText(cheques.get(tb.getSelectedRow()).getPrazo());
            txtVencimento.setText(String.valueOf(cheques.get(tb.getSelectedRow()).getVencimento()));
            txtCnpj.setText(cheques.get(tb.getSelectedRow()).getCnpj());
            txtConta.setText(cheques.get(tb.getSelectedRow()).getConta());
            txtValor.setText(String.valueOf(cheques.get(tb.getSelectedRow()).getValor()));

        } else {
            limpaCampos();
        }
    }

    protected void listarCheque() {
        ControleCheque che = new ControleCheque();
        cheques = che.listarCheques("%" + txtProcurar.getText().trim() + "%");
        mostrarCheque(cheques);
    }

    protected void mostrarCheque(List<BasicCheque> cheques) {

        while (tmCheques.getRowCount() > 0) {
            tmCheques.removeRow(0);
        }

        if (cheques.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Nenhum Registro Encontrado", "Erro", 0);
        } else {

            String[] campos = new String[]{null, null, null, null, null};
            for (int i = 0; i < cheques.size(); i++) {
                tmCheques.addRow(campos);

                tmCheques.setValueAt(cheques.get(i).getBanco(), i, 0);
                tmCheques.setValueAt(cheques.get(i).getAgencia(), i, 1);
                tmCheques.setValueAt(cheques.get(i).getConta(), i, 2);
                tmCheques.setValueAt(cheques.get(i).getValor(), i, 3);
                tmCheques.setValueAt(cheques.get(i).getCnpj(), i, 4);

            }
        }

    }

    protected boolean verificarCampos() {
        int a = txtBanco.getText().length();
        int b = txtAgencia.getText().length();
        int c = txtNumero.getText().length();
        int d = txtPrazo.getText().length();
        int e = txtVencimento.getText().length();
        int f = txtCnpj.getText().length();
        int g = txtConta.getText().length();
        int h = txtValor.getText().length();

        CnpjValido cnpj = new CnpjValido();
        CpfValido cpf = new CpfValido();

        if (a > 0 && a < 7) {
            if (b > 0 && b < 4) {
                if (c > 0 && c < 7) {
                    if (d > 0 && d < 4) {
                        if (e > 0 && e < 11) {
                            if (f > 13 && f < 19) {
                                if (cnpj.CNPJ(txtCnpj.getText().trim()) || cpf.CPF(txtCnpj.getText().trim())) {
                                    if (g > 0 && g < 6) {
                                        if (h > 0 && h < 10) {
                                            return true;
                                        } else {
                                            JOptionPane.showMessageDialog(rootPane, "Preencher o valor", "Aviso", 0);
                                            txtValor.requestFocus();
                                            return false;
                                        }
                                    } else {
                                        JOptionPane.showMessageDialog(rootPane, "Preencher  a conta", "Aviso", 0);
                                        txtConta.requestFocus();
                                        return false;
                                    }
                                } else {
                                    JOptionPane.showMessageDialog(rootPane, "Preencher um cnpj ou cpf Valido", "Aviso", 0);
                                    txtCnpj.requestFocus();
                                    return false;
                                }
                            } else {
                                JOptionPane.showMessageDialog(rootPane, "Preencher um cnpj ou cpf", "Aviso", 0);
                                txtCnpj.requestFocus();
                                return false;
                            }
                        } else {
                            JOptionPane.showMessageDialog(rootPane, "Preencher a data do vencimento", "Aviso", 0);
                            txtVencimento.requestFocus();
                            return false;
                        }
                    } else {
                        JOptionPane.showMessageDialog(rootPane, "Preencher um prazo", "Aviso", 0);
                        txtPrazo.requestFocus();
                        return false;
                    }
                } else {
                    JOptionPane.showMessageDialog(rootPane, "Preencher o numero do cheque", "Aviso", 0);
                    txtNumero.requestFocus();
                    return false;
                }
            } else {
                JOptionPane.showMessageDialog(rootPane, "Preencher a agencia", "Aviso", 0);
                txtAgencia.requestFocus();
                return false;
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "Preencher o numero do banco", "Aviso", 0);
            txtBanco.requestFocus();
            return false;
        }

    }

    protected void cadastraCheque() {

        if (verificarCampos()) {
            BasicCheque cheque = new BasicCheque();
            cheque.setBanco(txtBanco.getText().trim());
            cheque.setAgencia(txtAgencia.getText().trim());
            cheque.setNumeroCheque(txtNumero.getText().trim());
            cheque.setPrazo(txtPrazo.getText().trim());
            cheque.setVencimento(Date.valueOf(txtVencimento.getText().trim()));
            cheque.setCnpj(txtCnpj.getText().trim());
            cheque.setConta(txtConta.getText().trim());
            cheque.setValor(Double.parseDouble(txtValor.getText().trim()));

            ControleCheque c = new ControleCheque();
            c.cadastrarCheques(cheque);
            JOptionPane.showMessageDialog(this, "Registro feito com sucesso\n" + cheque.getNumeroCheque(), "Registro", 1);
            desabilitarCampos();
        }

    }

