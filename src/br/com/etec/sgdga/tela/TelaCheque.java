    private void limpaCampos() {
        txtBanco.setText("");
        txtAgencia.setText("");
        txtNumero.setText("");
        txtPrazo.setText("");
        txtVencimento.setText("");
        txtCnpj.setText("");
        txtConta.setText("");
        txtValor.setText("");
    }

    protected void alteraCheque() {
        if (tbCheque.getSelectedRow() != -1) {
            habilitarCampos();
        } else {
            JOptionPane.showMessageDialog(this, "Nenhum Registro Selecionado na Tabela", "Erro", 0);
        }
    }

    protected void alterarCheque() {
        if (verificarCampos()) {
            BasicCheque cheque = new BasicCheque();
            cheque.setId(cheques.get(tbCheque.getSelectedRow()).getId());

            cheque.setBanco(txtBanco.getText().trim());
            cheque.setAgencia(txtAgencia.getText().trim());
            cheque.setNumeroCheque(txtNumero.getText().trim());
            cheque.setPrazo(txtPrazo.getText().trim());
            cheque.setVencimento(Date.valueOf(txtVencimento.getText().trim()));
            cheque.setCnpj(txtCnpj.getText().trim());
            cheque.setConta(txtConta.getText().trim());
            cheque.setValor(Double.parseDouble(txtValor.getText().trim()));

            ControleCheque c = new ControleCheque();
            c.alterarCheques(cheque);
            JOptionPane.showMessageDialog(this, "Registro alterado com sucesso\n" + cheque.getNumeroCheque(), "Registro", 1);
            desabilitarCampos();
        }
    }

    private void novoCliente() {
        habilitarCampos();
        limpaCampos();
    }

    protected void excluirCheque() {
        if (tbCheque.getSelectedRow() != -1) {
            int res = JOptionPane.showConfirmDialog(this, "Deseja apagar este registro?", "Confirmação", JOptionPane.YES_NO_OPTION, 2);
            if (res == JOptionPane.YES_OPTION) {
                ControleCheque pc = new ControleCheque();
                pc.excluirCheques(cheques.get(tbCheque.getSelectedRow()).getId());
                JOptionPane.showMessageDialog(this, "Registro apagado com sucesso", "Registro", 0);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Nenhum Registro Selecionado na Tabela", "Erro", 0);
        }
    }

    //
    protected void atualizarCheque() {
        ControleCheque che = new ControleCheque();
        cheques = che.listarCheques("%%");
        ExibeCheque(cheques);
    }

    protected void ExibeCheque(List<BasicCheque> grup) {

        while (tmCheques.getRowCount() > 0) {
            tmCheques.removeRow(0);
        }

        if (!grup.isEmpty()) {

            String[] campos = new String[]{null, null};
            for (int i = 0; i < grup.size(); i++) {
                tmCheques.addRow(campos);

                tmCheques.setValueAt(cheques.get(i).getBanco(), i, 0);
                tmCheques.setValueAt(cheques.get(i).getAgencia(), i, 1);
                tmCheques.setValueAt(cheques.get(i).getConta(), i, 2);
                tmCheques.setValueAt(cheques.get(i).getValor(), i, 3);
                tmCheques.setValueAt(cheques.get(i).getCnpj(), i, 4);

            }
        }

    }

