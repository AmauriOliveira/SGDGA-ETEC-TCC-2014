    protected void tbFuncionarioLinhaSelecionada(JTable tb) {
        if (tb.getSelectedRow() != -1) {
            txtNome.setText(funcionarios.get(tb.getSelectedRow()).getNome());
            txtCpf.setText(funcionarios.get(tb.getSelectedRow()).getCpf());
            txtEndereco.setText(funcionarios.get(tb.getSelectedRow()).getEndereco());
            txtAdmicao.setText(String.valueOf(funcionarios.get(tb.getSelectedRow()).getAdmim()));
            txtFuncao.setText(funcionarios.get(tb.getSelectedRow()).getFuncao());
            txtFixo.setText(funcionarios.get(tb.getSelectedRow()).getFixo());
            txtCelular.setText(funcionarios.get(tb.getSelectedRow()).getCelular());

        } else {
            limpaCampos();
        }
    }

    protected void listarFuncionario() {
        ControleFuncionario func = new ControleFuncionario();
        funcionarios = func.listarFuncionarios("%" + txtPesquisar.getText().trim() + "%");
        mostrarFuncionario(funcionarios);
    }

    protected void mostrarFuncionario(List<BasicFuncionario> funcio) {

        while (tmFuncionarios.getRowCount() > 0) {
            tmFuncionarios.removeRow(0);
        }

        if (funcio.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Nenhum Registro Encontrado", "Erro", 0);
        } else {

            String[] campos = new String[]{null, null, null, null, null};
            for (int i = 0; i < funcio.size(); i++) {
                tmFuncionarios.addRow(campos);
                tmFuncionarios.setValueAt(funcio.get(i).getNome(), i, 0);
                tmFuncionarios.setValueAt(funcio.get(i).getCpf(), i, 1);
                tmFuncionarios.setValueAt(funcio.get(i).getCelular(), i, 2);
                tmFuncionarios.setValueAt(funcio.get(i).getFixo(), i, 3);
                tmFuncionarios.setValueAt(funcio.get(i).getFuncao(), i, 4);

            }
        }

    }

    protected boolean verificarCampos() {
        int a = txtNome.getText().trim().length();
        int b = txtCpf.getText().length();
        int c = txtEndereco.getText().length();
        int d = txtAdmicao.getText().length();
        int e = txtFuncao.getText().length();
        int f = txtFixo.getText().length();
        int g = txtCelular.getText().length();

        CpfValido cpf = new CpfValido();

        if (a > 0 && a < 26) {
            if (b == 14 & cpf.CPF(txtCpf.getText().trim())) {
                if (c > 0 && c < 46) {
                    if (d > 0 && d < 12) {
                        if (e > 0 && e < 16) {
                            if (f > 0 && f < 15) {
                                if (g > 0 && g < 16) {

                                    return true;
                                } else {
                                    JOptionPane.showMessageDialog(rootPane, "Preenchimento incorreto\nEste campo deve conter 15 caracteres", "Aviso", 0);
                                    txtCelular.requestFocus();
                                    return false;
                                }
                            } else {
                                JOptionPane.showMessageDialog(rootPane, "Preenchimento incorreto\nEste campo deve conter 14 caracteres", "Aviso", 0);
                                txtFixo.requestFocus();
                                return false;
                            }
                        } else {
                            JOptionPane.showMessageDialog(rootPane, "Preenchimento incorreto\nEste campo deve conter 15 caracteres", "Aviso", 0);
                            txtFuncao.requestFocus();
                            return false;
                        }
                    } else {
                        JOptionPane.showMessageDialog(rootPane, "Preenchimento incorreto\nEste campo deve conter 11 caracteres", "Aviso", 0);
                        txtAdmicao.requestFocus();
                        return false;
                    }
                } else {
                    JOptionPane.showMessageDialog(rootPane, "Preenchimento incorreto\nEste campo deve conter 45 caracteres", "Aviso", 0);
                    txtEndereco.requestFocus();
                    return false;
                }
            } else {
                JOptionPane.showMessageDialog(rootPane, "Preenchimento incorreto\nEste campo deve conter 14 caracteres", "Aviso", 0);
                txtCpf.requestFocus();
                return false;
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "Preenchimento incorreto\nEste campo deve conter 25 caracteres", "Aviso", 0);
            txtNome.requestFocus();
            return false;
        }
    }

    protected void cadastraFuncionario() {

        //SimpleDateFormat formato = new SimpleDateFormat("yyyy-MM-dd");
        // java.util.Date dataInico = (java.util.Date) txtAdmicao.getValue();
       // Date.valueOf(formato.format(dataInico));
        if (verificarCampos()) {
            BasicFuncionario funcionario = new BasicFuncionario();
            funcionario.setNome(txtNome.getText().trim());
            funcionario.setCpf(txtCpf.getText().trim());
            funcionario.setEndereco(txtEndereco.getText().trim());
            funcionario.setAdmim(Date.valueOf(txtAdmicao.getText().trim()));

            funcionario.setFuncao(txtFuncao.getText().trim());
            funcionario.setFixo(txtFixo.getText().trim());
            funcionario.setCelular(txtCelular.getText().trim());

            ControleFuncionario fun = new ControleFuncionario();
            fun.cadastrarFuncionarios(funcionario);
            JOptionPane.showMessageDialog(this, "Registro feito com sucesso\n" + funcionario.getNome(), "Registro", 1);
            desabilitarCampos();
        }

    }

