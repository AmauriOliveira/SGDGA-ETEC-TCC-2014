    private void limpaCampos() {

        txtNome.setText("");
        txtCpf.setText("");
        txtEndereco.setText("");
        txtAdmicao.setText("");
        txtFuncao.setText("");
        txtFixo.setText("");
        txtCelular.setText("");
    }

    protected void alteraFuncionario() {
        if (tbFuncionario.getSelectedRow() != -1) {
            habilitarCampos();
        } else {
            JOptionPane.showMessageDialog(this, "Nenhum Registro Selecionado na Tabela", "Erro", 0);
        }
    }

    protected void alterarFuncionario() {
        if (verificarCampos()) {
            BasicFuncionario funcionario = new BasicFuncionario();
            funcionario.setId(funcionarios.get(tbFuncionario.getSelectedRow()).getId());

            funcionario.setNome(txtNome.getText().trim());
            funcionario.setCpf(txtCpf.getText().trim());
            funcionario.setEndereco(txtEndereco.getText().trim());
            funcionario.setAdmim(Date.valueOf(txtAdmicao.getText().trim()));
            funcionario.setFuncao(txtFuncao.getText().trim());
            funcionario.setFixo(txtFixo.getText().trim());
            funcionario.setCelular(txtCelular.getText().trim());

            ControleFuncionario func = new ControleFuncionario();
            func.alterarFuncionarios(funcionario);
            JOptionPane.showMessageDialog(this, "Registro alterado com sucesso\n" + funcionario.getNome(), "Registro", 1);
            desabilitarCampos();
        }
    }

    private void novoFuncionario() {
        habilitarCampos();
        limpaCampos();
    }
//

    protected void atualizarFuncionario() {
        ControleFuncionario fun = new ControleFuncionario();
        funcionarios = fun.listarFuncionarios("%%");
        ExibeFuncionario(funcionarios);
    }

    protected void ExibeFuncionario(List<BasicFuncionario> funcio) {

        while (tmFuncionarios.getRowCount() > 0) {
            tmFuncionarios.removeRow(0);
        }

        if (funcio.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Nenhum Registro Encontrado", "Erro", 0);
        } else {

            String[] campos = new String[]{null, null, null, null, null};
            for (int i = 0; i < funcio.size(); i++) {
                tmFuncionarios.addRow(campos);
                tmFuncionarios.setValueAt(funcio.get(i).getNome(), i, 0);
                tmFuncionarios.setValueAt(funcio.get(i).getCpf(), i, 1);
                tmFuncionarios.setValueAt(funcio.get(i).getCelular(), i, 2);
                tmFuncionarios.setValueAt(funcio.get(i).getFixo(), i, 3);
                tmFuncionarios.setValueAt(funcio.get(i).getFuncao(), i, 4);

            }
        }

    }

    //
    protected void excluirFuncionario() {
        if (tbFuncionario.getSelectedRow() != -1) {
            int res = JOptionPane.showConfirmDialog(this, "Deseja apagar este registro?", "Confirmação", JOptionPane.YES_NO_OPTION, 2);
            if (res == JOptionPane.YES_OPTION) {
                ControleFuncionario fun = new ControleFuncionario();
                fun.excluirFuncionarios(funcionarios.get(tbFuncionario.getSelectedRow()).getId());
                JOptionPane.showMessageDialog(this, "Registro apagado com sucesso", "Registro", 0);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Nenhum Registro Selecionado na Tabela", "Erro", 0);
        }
    }
