    protected void tbNotaLinhaSelecionada(JTable tb) {
        if (tb.getSelectedRow() != -1) {

            txtId.setText(String.valueOf(notas.get(tb.getSelectedRow()).getId()));
            txtCliente.setText(String.valueOf(notas.get(tb.getSelectedRow()).getCliente()));
            txtData.setText(String.valueOf(notas.get(tb.getSelectedRow()).getData()));
            txtHora.setText(String.valueOf(notas.get(tb.getSelectedRow()).getHora()));
            txtValor.setText(String.valueOf(notas.get(tb.getSelectedRow()).getValor()));
            txtDesconto.setText(String.valueOf(notas.get(tb.getSelectedRow()).getDesconto()));
            txtPagoFinal.setText(String.valueOf(notas.get(tb.getSelectedRow()).getValorFinal()));
            listarItem();//preencher a tabela
        } else {
            //limpaCampos();
        }
    }

    protected void listarItem() {
        ControleItemNota ite = new ControleItemNota();
        items = ite.listarItemNotas(Integer.valueOf(txtId.getText().trim()));
        mostrarItems(items);
    }

    protected void mostrarItems(List<BasicItemNota> itemNotas) {

        while (tmItem.getRowCount() > 0) {
            tmItem.removeRow(0);
        }

        if (itemNotas.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Nenhum Registro Encontrado", "Erro", 0);
        } else {

            String[] campos = new String[]{null, null, null, null, null};
            for (int i = 0; i < itemNotas.size(); i++) {
                tmItem.addRow(campos);
                tmItem.setValueAt(itemNotas.get(i).getId(), i, 0);
                tmItem.setValueAt(itemNotas.get(i).getProduto(), i, 1);
                tmItem.setValueAt(itemNotas.get(i).getQuantidade(), i, 2);
                tmItem.setValueAt(itemNotas.get(i).getValorUni(), i, 3);
                tmItem.setValueAt(itemNotas.get(i).getSubTotal(), i, 4);

            }
        }

    }


