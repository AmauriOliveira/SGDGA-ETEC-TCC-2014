    private void pesquisarVendas() {

        ControleNota nota = new ControleNota();
        if (txtInicio.getValue() == null) {
            JOptionPane.showMessageDialog(this, "Informe a data inicial");
            txtInicio.requestFocus();
        } else {
            if (txtFinal.getValue() == null) {
                JOptionPane.showMessageDialog(this, "Informe a data de final");
                txtFinal.requestFocus();
            } else {
                SimpleDateFormat formato = new SimpleDateFormat("yyyy-MM-dd");
                java.util.Date dataInico = (java.util.Date) txtInicio.getValue();
                java.util.Date dataFim = (java.util.Date) txtFinal.getValue();

                notas = nota.listarVenda(Date.valueOf(formato.format(dataInico)), Date.valueOf(formato.format(dataFim)));

                String[] campos = new String[]{null, null, null, null, null};

                for (int i = 0; i < tmNota.getRowCount(); i++) {
                    tmNota.removeRow(i);
                }

                for (int i = 0; i < notas.size(); i++) {

                    tmNota.addRow(campos);

                    tmNota.setValueAt(notas.get(i).getId(), i, 0);
                    tmNota.setValueAt(notas.get(i).getCliente(), i, 1);
                    tmNota.setValueAt(notas.get(i).getData(), i, 2);
                    tmNota.setValueAt(notas.get(i).getHora(), i, 3);
                    tmNota.setValueAt(notas.get(i).getValorFinal(), i, 4);

                }
            }
        }

    }

